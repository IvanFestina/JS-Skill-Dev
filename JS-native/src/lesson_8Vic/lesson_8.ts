// 1. Функция sum принимает параметром целые положительные
// числа (неопределённое кол-во) и возвращает их сумму (rest).

export function sum(...nums: Array<any>): number {
    return nums.reduce((acc, numbers) => acc + numbers, 0)

}


// 2. Функция getTriangleType принимает три параметра:
// длины сторон треугольника.
// Функция должна возвращать:
//  - "10", если треугольник равносторонний,
//  - "01", если треугольник равнобедренный,
//  - "11", если треугольник обычный,
//  - "00", если такого треугольника не существует.

export function getTriangleType(a: number, b: number, c: number): string {
    const sumAB = a + b;
    const sumBC = b + c;
    const sumAC = a + c;
    return (
        (a > sumBC || b > sumAC || c > sumAB) ? '00'
            : (a === b && a === c) ? '10'
                : (a === b || b === c || a === c) ? '01'
                    : '11'
    )
}


// 3. Функция getSum принимает параметром целое число и возвращает
// сумму цифр этого числа

export function getSum(number: number): number {
    return Array.from(String(number), Number).reduce((acc, n) => acc + n, 0)
}


// 4. Функция isEvenIndexSumGreater принимает  параметром массив чисел.
// Если сумма чисел с чётными ИНДЕКСАМИ!!! (0 как чётный индекс) больше
// суммы чисел с нечётными ИНДЕКСАМИ!!!, то функция возвращает true.
// В противном случае - false.

export const isEvenIndexSumGreater = (arr: Array<number>): boolean => {
    let even = arr.map((n, index) => index % 2 === 0 ? n : 0).reduce((acc, n) => acc + n)
    let unEven = arr.map((n, index) => index % 2 === 1 ? n : 0).reduce((acc, n) => acc + n)
    return even > unEven
}

// 5. Функция getSquarePositiveIntegers принимает параметром массив чисел и возвращает новый массив. 
// Новый массив состоит из квадратов целых положительных чисел, котрые являются элементами исходгого массива.
// Исходный массив не мутирует.


export function getSquarePositiveIntegers(array: Array<number>): Array<number> {

    return array.filter(n => (n > 0 && (n % 1) === 0)).map(n => n ** 2)
}

